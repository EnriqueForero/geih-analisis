@dataclass
class ConfigComparativo:
    """Parámetros centrales del análisis comparativo GEIH.

    Único lugar que se modifica entre corridas.
    Valida en __post_init__ para fallar rápido si algo está mal.
    """
    # ── Rutas ──────────────────────────────────────────────────────
    ruta: str
    ruta_ciiu: str
    ruta_divipola: str

