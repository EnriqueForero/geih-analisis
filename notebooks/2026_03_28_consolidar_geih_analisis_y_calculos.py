        print(f"{'═'*55}")
        print(f"  Año base     : {self.anio_base} ({self.n_meses_base} meses)")
        print(f"  Año comp     : {self.anio_comp} ({self.n_meses_comp} meses)")
        print(f"  Comparar     : M01–M{self.n_meses_comparar:02d}")
        print(f"  Ruta datos   : {self.ruta}")
        print(f"{'═'*55}")


